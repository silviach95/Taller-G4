import math

def calcula_cos(value):
    return math.cos(value)

print ("Coseno_x{}".format(calcula_cos(20)))

def calcula_sen(value):
    return math.sin(value)

print ("Seno_x{}".format(calcula_sen(20)))

def calcula_tan(value):
    return math.tan(value)

print ("Tan_x{}".format(calcula_tan(20)))

def calcula_cosenh(value):
    return math.cosh(value)

print ("Cosenh_x{}".format(calcula_cosenh(20)))